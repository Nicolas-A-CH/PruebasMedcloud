public class CalcularIteraciones {

	// Método para calcular el número de iteraciones
	def calcularIteraciones(String horaInicio, String horaFin, int incrementoMinutos) {
		def formatoHora = new SimpleDateFormat("HH:mm")
		def inicio = formatoHora.parse(horaInicio)
		def fin = formatoHora.parse(horaFin)

		long diferencia = fin.time - inicio.time
		long minutosTotales = diferencia / (60 * 1000)

		return (minutosTotales / incrementoMinutos) as int
	}
}
